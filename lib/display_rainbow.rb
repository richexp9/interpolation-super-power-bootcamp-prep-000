def display_rainbow(colors)
  print "O: #{colors[1]},"
  print "R: #{colors[0]},"
  print "Y: #{colors[2]},"
  print "G: #{colors[3]},"
  print "B: #{colors[4]},"
  print "I: #{colors[5]},"
  print "V: #{colors[6]},"
end

display_rainbow(['red', 'orange', 'yellow', 'green', 'blue', 'indigo', 'violet'])
